def family_burden_level(yes_count: int, subjective_score: int) -> str:
    """客觀checklist計分（0-5）+ 主觀感受分數（0-5），總分0-10換算負荷等級"""
    total = yes_count + subjective_score
    if total <= 3:
        return "輕度負荷"
    if total <= 6:
        return "中度負荷"
    return "重度負荷"


def env_risk_level(risk_count: int) -> str:
    """依勾選「有風險」的項目數（排除不適用項目）換算風險等級"""
    if risk_count == 0:
        return "低風險"
    if risk_count <= 2:
        return "中風險"
    return "高風險"


# 依賴等級對照表（非衛福部CMS官方等級，是本系統依ADL/IADL分數換算的內部參考指標）。
# ADL欄位採巴氏量表原始0-100分；IADL欄位原表滿分為24分，因本系統IADL採Lawton量表
# （滿分31分），已依比例（31/24）換算並四捨五入為不重疊的整數區間。
# (序，僅供查表排序使用，不對外顯示, 依賴程度, ADL下限, ADL上限, IADL下限, IADL上限)
DEPENDENCY_LEVELS = [
    (1, "獨立", 100, 100, 31, 31),
    (2, "輕度依賴", 91, 99, 27, 30),
    (3, "中度依賴", 60, 90, 23, 26),
    (4, "中重度依賴", 30, 59, 19, 22),
    (5, "重度依賴", 15, 29, 15, 18),
    (6, "重度依賴", 10, 14, 11, 14),
    (7, "極重度依賴", 5, 9, 7, 10),
    (8, "極重度依賴", 0, 4, 0, 6),
]


def _level_by_score(score: int, key: str) -> int:
    for level, _label, adl_min, adl_max, iadl_min, iadl_max in DEPENDENCY_LEVELS:
        lo, hi = (adl_min, adl_max) if key == "adl" else (iadl_min, iadl_max)
        if lo <= score <= hi:
            return level
    return 8


def dependency_level(adl_score: int, iadl_score: int) -> str:
    """依ADL與IADL分數分別查表得等級，取較嚴重（數字較大）者，回傳依賴程度文字
    （不顯示序號，避免與衛福部CMS官方等級混淆）。"""
    adl_lv = _level_by_score(adl_score, "adl")
    iadl_lv = _level_by_score(iadl_score, "iadl")
    final_level = max(adl_lv, iadl_lv)
    return next(label for level, label, *_ in DEPENDENCY_LEVELS if level == final_level)
